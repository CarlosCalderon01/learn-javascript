/**
    "Type Casting" (o conversión de tipo) se refiere a la transferencia de datos de un tipo de dato a
        otro. Esto es común en la programación cuando trabajas con diferentes tipos de datos y
        necesitas asegurarte de que los datos se encuentren en el formato adecuado para realizar
        ciertas operaciones.

    Conversión implícita: la conversión de tipo puede
        ocurrir automáticamente sin que el programador tenga que hacer nada explícitamente. Esto
        se conoce como "conversión implícita" y ocurre cuando el lenguaje de programación
        convierte automáticamente un tipo de dato en otro para permitir ciertas operaciones. Por
        ejemplo, en JavaScript, puedes sumar un número y una cadena, y JavaScript convertirá
        automáticamente la cadena en un número si es posible.
   
**/

// Ejemplo de conversión implícita en JavaScript:

let numero = 5;
let cadena = "10";
let resultado = numero + cadena; // JavaScript convierte "10" en número y realiza la suma.
console.log(resultado); // Resultado: 15 (un número)

