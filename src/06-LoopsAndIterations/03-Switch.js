const dia = 'martes'

switch (dia) {
  case 'lunes':
    console.log('Es el primer día de la semana.')
    break
  case 'martes':
    console.log('Es el segundo día de la semana.')
    break
  case 'miércoles':
    console.log('Es el tercer día de la semana.')
    break
  case 'jueves':
    console.log('Es el cuarto día de la semana.')
    break
  case 'viernes':
    console.log('Es el quinto día de la semana.')
    break
  case 'sábado':
  case 'domingo':
    console.log('Es un fin de semana.')
    break
  default:
    console.log('Ese no es un día válido.')
}

